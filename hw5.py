from pathlib import Path
import pathlib
import numpy as np
from typing import Union
import pandas as pd
import matplotlib.pyplot as plt
from typing import Tuple

class QuestionnaireAnalysis:
    """
    Reads and analyzes data generated by the questionnaire experiment.
    Should be able to accept strings and pathlib.Path objects.
    """

    def __init__(self, data_fname: Union[pathlib.Path, str]):
        self.data_fname = pathlib.Path(data_fname)
        if not self.data_fname.exists():
            raise(ValueError)

    def read_data(self):
        """Reads the json data located in self.data_fname into memory, to
        the attribute self.data.
        """
        self.data = pd.read_json(self.data_fname)

    def show_age_distrib(self) -> Tuple[np.ndarray, np.ndarray]:
        """Calculates and plots the age distribution of the participants.

        Returns
        -------
        hist : np.ndarray
        Number of people in a given bin
        bins : np.ndarray
        Bin edges
            """
        
        all_participants_ages = self.data.loc[:,"age"]
        all_participants_ages = all_participants_ages.dropna()
        np_hist = np.histogram(all_participants_ages, bins=10, range=(0,100))
        hist = np_hist[0]
        bins = np_hist[1]
        return (hist, bins)

    def remove_rows_without_mail(self) -> pd.DataFrame:
        """Checks self.data for rows with invalid emails, and removes them.

        Returns
        -------
        df : pd.DataFrame
        A corrected DataFrame, i.e. the same table but with the erroneous rows removed and
        the (ordinal) index after a reset.
            """
        df = self.data
        df = df[df["email"].str.count("@") == 1 & ~df['email'].str.startswith('@') & ~df['email'].str.endswith('@')]
        df = df[df["email"].str.contains("\\.") & ~df['email'].str.startswith('\\.') & ~df['email'].str.endswith('\\.')]
        df = df[~df['email'].str.contains('@\\.')]


        return df.reset_index()
    
    def fill_na_with_mean(self) -> Tuple[pd.DataFrame, np.ndarray]:
        """Finds, in the original DataFrame, the subjects that didn't answer
        all questions, and replaces that missing value with the mean of the
        other grades for that student.

        Returns
        -------
        df : pd.DataFrame
        The corrected DataFrame after insertion of the mean grade
        arr : np.ndarray
            Row indices of the students that their new grades were generated
            """
        
        df = self.data

        nan_q1 = np.where(df["q1"].isna())[0] 
        nan_q2 = np.where(df["q2"].isna())[0] 
        nan_q3 = np.where(df["q3"].isna())[0] 
        nan_q4 = np.where(df["q4"].isna())[0] 
        nan_q5 = np.where(df["q5"].isna())[0] 

        arr = np.concatenate((nan_q1,nan_q2,nan_q3,nan_q4,nan_q5))
        arr.sort()
        arr = np.unique(arr)

        # fill mean where values are nan
        df['mean'] = df.iloc[:, 8:12].mean(axis=1)
        df['q1'] = df['q1'].fillna(df['mean'])
        df['q2'] = df['q2'].fillna(df['mean'])
        df['q3'] = df['q3'].fillna(df['mean'])
        df['q4'] = df['q4'].fillna(df['mean'])
        df['q5'] = df['q5'].fillna(df['mean'])

        df.drop(columns=['mean'], inplace=True)

        return (df,arr)
    
    def score_subjects(self, maximal_nans_per_sub: int = 1) -> pd.DataFrame:
        """Calculates the average score of a subject and adds a new "score" column
        with it.

        If the subject has more than "maximal_nans_per_sub" NaN in his grades, the
        score should be NA. Otherwise, the score is simply the mean of the other grades.
        The datatype of score is UInt8, and the floating point raw numbers should be
        rounded down.

        Parameters
        ----------
        maximal_nans_per_sub : int, optional
            Number of allowed NaNs per subject before giving a NA score.

        Returns
        -------
        pd.DataFrame
            A new DF with a new column - "score".
        """
        df = self.data
        df['score'] = np.nan # all nans
        df['mean_questions'] = df.loc[:, 'q1':'q5'].mean(axis=1) # mean of all rows
        df['mean_questions'] = df['mean_questions'].apply(np.floor)
        cols = ['q1', 'q2', 'q3', 'q4', 'q5'] # count nan by row
        df['number_of_NaNs'] = df[cols].isna().sum(1) #counts nans in each row
        df.loc[df['number_of_NaNs'] <= maximal_nans_per_sub, 'score'] = df['mean_questions'] 
        df.drop(columns=['mean_questions','number_of_NaNs'], inplace=True)
        df = df.astype({'score': 'UInt8'})

        return df


    def correlate_gender_age(self) -> pd.DataFrame:
        """Looks for a correlation between the gender of the subject, their age
        and the score for all five questions.

        Returns
        -------
        pd.DataFrame
            A DataFrame with a MultiIndex containing the gender and whether the subject is above
            40 years of age, and the average score in each of the five questions.
        """
        df = self.data
        df.dropna(subset = ['age'], inplace = True)
        df['above_40'] = False # all nans
        df.loc[df['age'] > 40, 'above_40'] = True
        df['age'] = df['above_40']
        df = df.set_index(['gender','age'])
        df = df.groupby(['gender', 'age']).agg({'q1': lambda x: x.astype('Float64').mean(), 'q2': lambda x: x.astype('Float64').mean(), 'q3': lambda x: x.astype('Float64').mean(), 'q4': lambda x: x.astype('Float64').mean(), 'q5': lambda x: x.astype('Float64').mean()})
        print(df)
        return df

